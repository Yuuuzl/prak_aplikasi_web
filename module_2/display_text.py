import streamlit as st

# TEXT ELEMENTS 

# Markdown
# Heading
st.markdown("# This is a heading")
st.markdown("## This is a subheading")
st.markdown("### This is a subsubheading")
# Bold
st.markdown("**This is bold text**")
# Italic
st.markdown("*This is italic text*")
# Blockquote
st.markdown("> This is a blockquote")
# Ordered list
st.markdown("1. First item")
st.markdown("2. Second item")
st.markdown("3. Third item")
# Unordered list
st.markdown("- First item")
st.markdown("- Second item")
st.markdown("- Third item")
# Code
st.markdown("`print('Hello world')`")
# Horizontal rule
st.markdown("---")

#Title
st.title("This is a title")

#Header
st.header("This is a header")

#Subheader
st.subheader("This is a subheader")

#Caption
st.caption("This is a caption")

#Text
st.text("This is a text")

